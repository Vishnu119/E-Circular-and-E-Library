package com.example.firebasedemo;

public class loadpdf {
    public String name;
    public String url;

    public loadpdf() {
    }

    public loadpdf(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }
}
